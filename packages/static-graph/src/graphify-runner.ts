/**
 * G1 — Graphify runner
 *
 * Detects Graphify installation and executes it as a Python subprocess.
 * Copies the output files (graph.json, graph.html, GRAPH_REPORT.md) to
 * .tracegraph/static-graph/graphify/ after a successful run.
 *
 * The build command is fully configurable via staticGraph.buildCommand.
 * Default: "graphify . --directed"
 *
 * Auto-retry behaviour:
 *   If Graphify exits with "no LLM API key found", the repo contains non-code
 *   files (docs, images, PDFs) that require a backend for semantic extraction.
 *   We automatically retry with a temporary .graphifyignore that restricts
 *   analysis to code files only — no API key needed for that corpus.
 */
import * as fs           from 'fs';
import * as path         from 'path';
import { spawnSync }     from 'child_process';
import { graphifyDir, rawGraphPath, GRAPHIFY_GITIGNORE_CONTENT } from './graph-dir';

// ─── Detection ───────────────────────────────────────────────────────────────

export type GraphifyDetection = {
  found:   boolean;
  version: string | null;
  /** The command that worked (e.g. "graphify --version"). */
  cmd:     string[] | null;
};

/**
 * Detect a working Graphify installation.
 *
 * graphifyy is installed into an isolated environment by uv/pipx, so
 * `python -m graphify` will never resolve — only the `graphify` binary
 * placed on PATH by the installer works.
 */
export function detectGraphify(): GraphifyDetection {
  try {
    const result = spawnSync('graphify', ['--version'], {
      encoding: 'utf8',
      stdio:    'pipe',
      timeout:  8_000,
      shell:    process.platform === 'win32',
    });
    if (result.status === 0) {
      const version = parseVersion(result.stdout ?? result.stderr ?? '');
      return { found: true, version, cmd: ['graphify', '--version'] };
    }
  } catch { /* not in PATH */ }

  return { found: false, version: null, cmd: null };
}

/** Parse a version string like "graphify 0.4.1" → "0.4.1" */
function parseVersion(output: string): string | null {
  const m = output.match(/(\d+\.\d+(?:\.\d+)?)/);
  return m ? m[1]! : output.trim().split('\n')[0] ?? null;
}

